public class C02_변수 {
    public static void main(String[] args) {
        int count; // int : 타입 , count : 변수 이름
        count = 10; //변수 count를 10으로 초기화
        count = 20; //변수 count를 20으로 재초기화
        System.out.println("count \t = " + count);

        double avg = 11.7; //변수 선언과 동시에 초기화
        System.out.println("avg \t = " + avg);
    }
}
