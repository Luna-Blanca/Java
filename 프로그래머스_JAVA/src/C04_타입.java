public class C04_타입 {
    public static void main(String[] args) {
        boolean inFun = true;
        System.out.println("inFun = " + inFun);

        char c = 'f';
        System.out.println("c = " + c);

        long big = 100000L;
        System.out.println("big = "  + big);

        float f = 2.193821f;
        System.out.println("f = " + f);

        double d = 2.1938212193821;
        System.out.println("d = " + d);
    }
}
