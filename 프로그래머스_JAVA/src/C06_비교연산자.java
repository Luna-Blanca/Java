public class C06_비교연산자 {
    public static void main(String[] args) {
        int i = 10;
        int j = 10;

        System.out.println("i == j : " + (i == j));
        System.out.println("i != j : " + (i != j));
        System.out.println("i < j : "  + (i < j));
        System.out.println("i =< j : " + (i <= j));
        System.out.println("i > j : " +  (i > j));
        System.out.println("i >= j : " + (i >= j));
    }
}
