package DAY_13;

public class InfoObject {
    // FIELD
    private String name;
    private int money;

    // METHOD
    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getMoney() {
        return money;
    }
}
